// ── Вспомогательные функции ───────────────────────────────────────────────────

/** Обрезает текст до maxLen символов, не разрывая слово */
function short(text, maxLen = 80) {
  if (!text) return '';
  const s = text.trim().replace(/\s+/g, ' ');
  if (s.length <= maxLen) return s;
  return s.slice(0, maxLen).replace(/\s\S*$/, '') + '…';
}

/** Первое предложение из ответа */
function firstSentence(text) {
  if (!text) return '';
  const m = text.match(/^[^.!?]+[.!?]?/);
  return m ? m[0].trim() : short(text, 100);
}

/** Тест регулярки без учёта регистра */
function has(text, pattern) {
  return pattern.test((text || '').toLowerCase());
}

// ── Определение типажа ────────────────────────────────────────────────────────
function detectTypology(qa) {
  const q1  = qa.q1  || '';
  const q4  = qa.q4  || '';
  const q5  = qa.q5  || '';
  const q6  = qa.q6  || '';
  const q7  = qa.q7  || '';
  const q8  = qa.q8  || '';
  const q14 = qa.q14 || '';
  const q15 = qa.q15 || '';

  // Типаж A — «Замороженный в просадке»
  let scoreA = 0;
  if (has(q8, /агресси|злост|бесит|раздража|злюсь|злой|бешус|теряюсь|не знаю что|ступор/))  scoreA += 3;
  if (has(q7, /тревог|страшно|мяться|неловко|стыдно|боюсь|неуверен|сжимается|страх|тяжело|напряж/)) scoreA += 3;
  if (has(q1, /упал|упало|меньше|снизил|просадк|не расту|стои[тл]\s|застрял|потолок|топчусь/))      scoreA += 2;
  if (has(q6, /страх|боюсь|мяться|неловко|называть цену|называю цену/))                             scoreA += 2;

  // Типаж B — «Системный с гиперответственностью»
  let scoreB = 0;
  if (has(q8, /улучши|доработ|не хватает|компетенц|проработ|больше знат|научить|прокачат/))          scoreB += 3;
  if (has(q14 + q15, /не заслужил|ленив|сам виноват|виноват сам|надо больше|должен больше/))          scoreB += 3;
  if (has(q4 + q5, /системн|методолог|разрабатыва|комплексн|процесс|структур/))                      scoreB += 2;
  if (has(q15, /себя|сам себе|мешаю себе|голова|мысли/))                                             scoreB += 1;

  // Типаж C — «Готовый без системы»
  let scoreC = 0;
  if (has(q6 + q15, /нет системы|хаос|непоследовательн|нет плана|непредсказуем|не понимаю откуда|случайн/)) scoreC += 3;
  if (has(q5, /не знаю кому|всем|разным|кто угодно|любой|широкая/))                                 scoreC += 2;
  if (has(q6, /нет системы|хаос|нет понимания|не знаю как|не выстрое/))                             scoreC += 2;
  if (scoreA === 0 && scoreB === 0 && q4 && q5) scoreC += 1; // есть продукт, но нет других маркеров

  const max = Math.max(scoreA, scoreB, scoreC);
  if (max === 0) return 'A'; // fallback
  if (scoreA === max) return 'A';
  if (scoreB === max) return 'B';
  return 'C';
}

// ── Выбор кейса по типажу и нише ─────────────────────────────────────────────
function selectCase(typology, qa, name) {
  const sphere = ((qa.q4 || '') + ' ' + (qa.q5 || '') + ' ' + (name || '')).toLowerCase();

  // По нише
  if (/веб|сайт|разработ|программ|it\b|developer|frontend|backend/i.test(sphere)) return CASES.fedor;
  if (/таргет|smm|трафик|реклам|instagram|вконтакт|tiktok/i.test(sphere))          return CASES.samat;
  if (/дизайн|брендинг|айдентик|визуал/i.test(sphere))                             return CASES.marat;
  if (/b2b|агентств|клиент.*бизнес|бизнес.*клиент|корпоратив/i.test(sphere))       return CASES.oleg;
  if (/с нуля|начинаю|только начал|первые клиенты|нет клиентов/i.test(sphere))     return CASES.egor;

  // По типажу
  if (typology === 'A') return CASES.marat;
  if (typology === 'B') return CASES.oleg;
  return CASES.samat;
}

// ── Определить «зацепившее» — самый сильный ответ ────────────────────────────
function getHook(qa) {
  // Ищем самый конкретный и эмоциональный ответ
  const candidates = [
    { q: qa.q7,  hint: 'когда называешь цену' },
    { q: qa.q8,  hint: 'реакция на «дорого»' },
    { q: qa.q15, hint: 'что мешает прямо сейчас' },
    { q: qa.q6,  hint: 'где обрывается путь к деньгам' },
    { q: qa.q11, hint: 'потерянный клиент' },
  ];
  // Выбираем самый длинный (скорее всего — самый конкретный)
  const best = candidates
    .filter(c => c.q && c.q.trim().length > 20)
    .sort((a, b) => b.q.length - a.q.length)[0];
  return best || null;
}

// ── Собираем 3 ключевых маркера с цитатами ────────────────────────────────────
function buildMarkers(typology, qa) {
  const markers = [];

  // Маркер 1 — цена / деньги (Q7)
  if (qa.q7 && qa.q7.trim().length > 10) {
    markers.push({
      quote: short(qa.q7, 90),
      insight: typology === 'A'
        ? 'Вот откуда уходят деньги. Не из-за ниши и не из-за рынка — а именно здесь. Когда тело реагирует раньше, чем голова успевает выстроить позицию, клиент считывает это мгновенно.'
        : typology === 'B'
        ? 'Слышу в этом не страх, а усиленный самоконтроль. Ты думаешь «я должен быть увереннее» — но это не про уверенность. Это про то, где именно ты теряешь рамку.'
        : 'Это нормально для человека без системы ценообразования. Цена должна быть выведена из логики, а не из ощущений — тогда называть её легко.',
    });
  }

  // Маркер 2 — реакция на «дорого» (Q8)
  if (qa.q8 && qa.q8.trim().length > 10) {
    markers.push({
      quote: short(qa.q8, 90),
      insight: typology === 'A'
        ? 'Эта реакция — прямой маркер просадки. Не «плохой» маркер — просто показывает точку, в которой деньги уходят мимо. Здесь начинается работа.'
        : typology === 'B'
        ? 'Ты берёшь ответственность туда, где её быть не должно. «Дорого» — это не про твой продукт. Это про картину мира клиента. Работать с этим — твоя задача, но не через улучшение себя.'
        : 'Нет системы работы с возражением — нет и стабильного результата. Одни и те же слова, сказанные с позицией, закрывают. Сказанные без позиции — нет.',
    });
  }

  // Маркер 3 — главный барьер (Q15 или Q6)
  const barrier = qa.q15 || qa.q6;
  if (barrier && barrier.trim().length > 10 && markers.length >= 1) {
    markers.push({
      quote: short(barrier, 90),
      insight: typology === 'A'
        ? 'Это не причина — это симптом. За этим стоит конкретный паттерн, который мы разберём на созвоне. Там обычно 1-2 ключевых момента — не список из 10 пунктов.'
        : typology === 'B'
        ? 'Слышу знакомое: ты снова берёшь это на себя. Это не вопрос усилий — ты и так работаешь. Это вопрос системы, которая работает без постоянного давления на себя.'
        : 'Вот оно — нет системы. Продукт есть, умение есть, а вот воронки и повторяемости нет. Это решаемо конкретными шагами, не «работой над собой».',
    });
  }

  return markers.slice(0, 3);
}

// ── Конкретный шаг на неделю по типажу ───────────────────────────────────────
function buildWeeklyStep(typology, qa) {
  const income = qa.q1 || '';
  const q10    = qa.q10 || ''; // последний раз когда называл цену

  if (typology === 'A') {
    return `На эту неделю — одно действие: найди 2-3 диалога с клиентами, где ты почувствовал что «съехал» с цены или начал оправдываться. Выпиши дословно что говорил. Это покажет конкретный паттерн — и с него начнём.`;
  }
  if (typology === 'B') {
    return `На эту неделю: напиши одно предложение — что ты даёшь клиенту и какой конкретный результат он получает. Без «системного подхода» и «комплексного решения». Один конкретный результат — одна цена. Это точка входа.`;
  }
  return `На эту неделю: опиши своего идеального клиента в одном абзаце. Не «малый бизнес» и не «все кому нужен таргет». Конкретно: ниша, боль, ситуация. Это первый шаг к предсказуемому потоку.`;
}

// ── Главная функция ───────────────────────────────────────────────────────────
export function generateDraft(lead) {
  const qa   = lead.quiz_answers || {};
  const name = (lead.name || 'Привет').split(' ')[0]; // только имя

  const typology  = detectTypology(qa);
  const caseRec   = selectCase(typology, qa, lead.name);
  const hook      = getHook(qa);
  const markers   = buildMarkers(typology, qa);
  const weekStep  = buildWeeklyStep(typology, qa);

  // ── Типажные блоки ─────────────────────────────────────────────────────────
  const typologyBlock = {
    A: {
      label: 'Типаж A — Замороженный в просадке',
      desc: `Вижу человека с экспертизой и опытом — но где-то внутри стоит стопор, и он начинает стоить денег. Это не про навыки. Это про конкретный момент, где ты теряешь позицию — и клиент это считывает.`,
    },
    B: {
      label: 'Типаж B — Системный с гиперответственностью',
      desc: `Вижу человека, который умеет работать и работает хорошо. Но берёт ответственность туда, куда не надо — и это тормозит рост. Дело не в том, чтобы «стать лучше». Дело в том, чтобы убрать один конкретный паттерн.`,
    },
    C: {
      label: 'Типаж C — Готовый без системы',
      desc: `Вижу человека, у которого есть продукт и есть навык. Не хватает системы — повторяемости, предсказуемости, понятного пути клиента. Не «работы над собой», а конкретной конструкции.`,
    },
  }[typology];

  // ── Сборка черновика ───────────────────────────────────────────────────────
  const lines = [];

  // 1. Обращение + зацепка
  if (hook) {
    lines.push(`${name}, прочитал твои ответы.`);
    lines.push('');
    lines.push(`Особенно зацепило вот это — про ${hook.hint}:`);
    lines.push(`«${short(hook.q, 100)}»`);
    lines.push('');
    lines.push('Это не случайная деталь. Это ключ.');
  } else {
    lines.push(`${name}, прочитал твои ответы. Есть что разобрать — конкретно, не в общем.`);
  }
  lines.push('');

  // 2. Типаж
  lines.push(typologyBlock.label + ':');
  lines.push('');
  lines.push(typologyBlock.desc);
  lines.push('');

  // 3. Маркеры с цитатами
  if (markers.length > 0) {
    lines.push('Три точки, которые вижу в твоих ответах:');
    lines.push('');
    markers.forEach((m, i) => {
      lines.push(`${i + 1}. «${m.quote}»`);
      lines.push('');
      lines.push(m.insight);
      lines.push('');
    });
  }

  // 4. Кейс
  lines.push(`Ближайший похожий случай из тех, с кем работал:`);
  lines.push('');
  lines.push(caseRec.text);
  lines.push('');

  // 5. Шаг на неделю
  lines.push(weekStep);
  lines.push('');

  // 6. CTA
  lines.push(`Если откликнулось — давай созвонимся 30 минут, посмотрим конкретно на твою ситуацию.`);
  lines.push('');

  // 7. Подпись
  lines.push('— Шамиль');

  return {
    draft    : lines.join('\n'),
    typology,
    case_name: caseRec.name,
  };
}
