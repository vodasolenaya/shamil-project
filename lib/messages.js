// Configurable offer variables — ставить через Vercel env vars:
//   NEXT_COHORT_DATE  = "19 мая"
//   SLOTS_LEFT        = "5"
//   MAX_STUDENTS      = "12"

const BOOKING_URL = 'https://artofsales.art/diagnostic';

function offerVars() {
  return {
    next_cohort_date : process.env.NEXT_COHORT_DATE  || '19 мая',
    slots_left       : process.env.SLOTS_LEFT        || '5',
    max_students     : process.env.MAX_STUDENTS      || '12',
  };
}

// Кнопка «Записаться» — inline keyboard для Telegram
function bookBtn(label = 'Записаться на разбор') {
  return {
    inline_keyboard: [[{ text: label, url: BOOKING_URL }]],
  };
}

