/**
 * buildTripwireAccess({ name })
 * Отправляется через ~5 минут после оплаты трипваера (3 000₽).
 * Подтверждение оплаты + доступ к урокам.
 * Если задан TRIPWIRE_LESSONS_URL — даёт прямую ссылку.
 */
export function buildTripwireAccess({ name }) {
  const firstName  = (name || 'Привет').split(' ')[0];
  const lessonsUrl = process.env.TRIPWIRE_LESSONS_URL || '';
  const lines = [
    `${firstName}, оплата получена. Спасибо!`,
    '',
  ];
  if (lessonsUrl) {
    lines.push(`Вот доступ к урокам: ${lessonsUrl}`);
  } else {
    lines.push(`Шамиль пришлёт доступ к урокам в течение часа.`);
    lines.push(`Если не получил — напиши: @vodasolenaya`);
  }
  lines.push('', '— бот Шамиля');
  return lines.join('\n');
}

