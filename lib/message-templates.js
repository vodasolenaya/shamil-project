/**
 * lib/message-templates.js
 * 3 шаблона первого сообщения для директ-помощника в админке.
 * Шамиль выбирает один, редактирует и отправляет кнопкой.
 * Стиль: прямой, без emoji в теле, без инфобизнеса — @localframe.
 */

/**
 * buildTemplates({ name, q5, q7, q1, case_name, case_result, case_before })
 * Возвращает 3 готовых шаблона текстом.
 */
export function buildTemplates({ name, q5 = '', q7 = '', q1 = '', case_name = '', case_result = '', case_before = '' }) {
  const firstName = (name || 'Привет').split(' ')[0];

  // Коротко обрезаем длинные ответы
  const q5short = q5.length > 150 ? q5.slice(0, 150).trim() + '...' : q5;
  const q7short = q7.length > 100 ? q7.slice(0, 100).trim() + '...' : q7;

  return [
    {
      label: 'Персональный — через барьер',
      text: [
        `${firstName}, привет. Прочитал твои ответы.`,
        '',
        `Одна вещь зацепила: «${q5short || q7short || 'что-то держит на одном уровне'}».`,
        '',
        `Это не абстрактно. Это конкретный барьер — и он встречается у людей с похожей историей.`,
        '',
        `${case_name ? `${case_name} был в похожей ситуации — ${case_before}. За месяц — ${case_result}.` : ''}`,
        '',
        `Хочу разобрать твою ситуацию — это займёт 10 минут письменно или 30 в звонке. Что удобнее?`,
        '',
        '— Шамиль',
      ].filter((_, i, arr) => !(arr[i] === '' && arr[i-1] === '')).join('\n'),
    },
    {
      label: 'Один вопрос',
      text: [
        `${firstName}, привет. Прочитал диагностику.`,
        '',
        `Один вопрос, честно: «${q5short || q7short || '...'}» — это про что сейчас больше? Про деньги, про время, или про что-то ещё?`,
        '',
        `Не тороплю. Просто хочу понять точнее, прежде чем что-то предлагать.`,
        '',
        '— Шамиль',
      ].join('\n'),
    },
    {
      label: 'Прямо на созвон',
      text: [
        `${firstName}, прочитал ответы.`,
        '',
        `Вижу несколько мест, где теряются деньги — конкретно у тебя, не в общем.`,
        '',
        `Давай 30 минут? Я покажу где и что можно сдвинуть прямо сейчас. Бесплатно.`,
        '',
        `Когда удобно на этой неделе?`,
        '',
        '— Шамиль',
      ].join('\n'),
    },
  ];
}

/**
 * buildCallFollowup({ name })
 */
