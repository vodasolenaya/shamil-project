/**
 * lib/message-templates.js
 * 3 шаблона первого сообщения для директ-помощника в админке.
 * Шамиль выбирает один, редактирует и отправляет кнопкой.
 * Стиль: прямой, без emoji в теле, без инфобизнеса — @localframe.
 */

/**
 * buildTemplates({ name, q5, q7, q1, case_name, case_result, case_before })
 * Возвращает 3 готовых шаблона текстом.
 */
export function buildTemplates({ name, q5 = '', q7 = '', q1 = '', case_name = '', case_result = '', case_before = '' }) {
  const firstName = (name || 'Привет').split(' ')[0];

  // Коротко обрезаем длинные ответы
  const q5short = q5.length > 150 ? q5.slice(0, 150).trim() + '...' : q5;
  const q7short = q7.length > 100 ? q7.slice(0, 100).trim() + '...' : q7;

  return [
    {
      label: 'Персональный вход',
      text: [
        `${firstName}, привет. Прочитал твои ответы на диагностику.`,
        '',
        `Вот что зацепило: «${q5short || q7short || 'ты сам написал что что-то держит'}».`,
        '',
        `Это не про скилы. Это про то, что внутри стоит конкретный барьер — и он мешает называть цену спокойно.`,
        '',
        `У ${case_name} была похожая картина — ${case_before}. За месяц — ${case_result}.`,
        '',
        `Расскажу подробно, если интересно?`,
        '',
        '— Шамиль',
      ].join('\n'),
    },
    {
      label: 'Вопрос-уточнение',
      text: [
        `${firstName}, привет. Это Шамиль, ты проходил у меня диагностику.`,
        '',
        `Один вопрос по твоим ответам: ты написал «${q5short || q7short || '...'}».`,
        '',
        `Что конкретно хочешь изменить в ближайший месяц? Не спеши с ответом — подумай, это важнее чем быстро.`,
        '',
        '— Шамиль',
      ].join('\n'),
    },
    {
      label: 'Приглашение на разбор',
      text: [
        `${firstName}, прочитал твои ответы.`,
        '',
        `Вижу несколько мест, где теряются деньги — конкретно у тебя, не в общем.`,
        '',
        `Давай проведём 30-минутный разбор — я покажу где именно, и что можно сделать прямо сейчас. Бесплатно.`,
        '',
        `Когда удобно на этой неделе?`,
        '',
        '— Шамиль',
      ].join('\n'),
    },
  ];
}

/**
 * buildCallFollowup({ name })
 */
export function buildCallFollowup({ name }) {
  const firstName = (name || 'Привет').split(' ')[0];
  return [
    `${firstName}, не спешу с ответом — но вчера мы говорили, и я хочу спросить одну вещь.`,
    '',
    `Что ты почувствовал после созвона? Какое слово точнее всего описывает состояние: «надо подумать», «сомневаюсь», «не мой формат», или что-то ещё?`,
    '',
    `Скажи честно. Если «не мой формат» — я пойму, так бывает. Если «надо подумать» — я помогу сформулировать что именно останавливает.`,
    '',
    '— Шамиль',
  ].join('\n');
}

/**
 * buildCallReminder({ name, call_time, zoom_url, type })
 * type: '24h' | '2h' | '15min'
 */
export function buildCallReminder({ name, call_time, zoom_url, type }) {
  const firstName = (name || 'Привет').split(' ')[0];

  if (type === '24h') {
    return [
      `${firstName}, напоминаю — завтра в ${call_time} у нас созвон.`,
      '',
      `Чтобы не тратить время — 3 вопроса для подготовки. Запиши куда-нибудь ответы (или бросай сюда, посмотрю):`,
      '',
      `1. Последняя сделка, которую ты потерял — что именно сказал клиент, что ты ответил?`,
      `2. Какой результат за этот месяц сделает созвон «не зря»?`,
      `3. Если бы я сегодня сказал тебе «я вижу в чём проблема» — ты услышал бы это как обвинение или как помощь?`,
      '',
      'До завтра.',
      '',
      '— Шамиль',
    ].join('\n');
  }

  if (type === '2h') {
    return [
      `Через 2 часа говорим. Ссылка: ${zoom_url}`,
      '',
      `Если что-то изменилось — скажи сейчас, перенесём. Если всё в силе — жду.`,
    ].join('\n');
  }

  if (type === '15min') {
    return [
      `Начинаем через 15 минут.`,
      '',
      zoom_url,
      '',
      `Можно заходить сейчас — буду ждать.`,
    ].join('\n');
  }

  return '';
}
